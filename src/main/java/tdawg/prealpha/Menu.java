    private void ConvertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConvertButtonActionPerformed

    String tickerCurrency1 = jComboBox1.getSelectedItem().toString();
    String tickerCurrency2 = jComboBox2.getSelectedItem().toString();
    String result;
    String formattedPrice;
    String formattedAmount;
    Double price;
    Double amount = 0.0;
    DecimalFormat format = new DecimalFormat("#0.00");
    
    try{
        amount = Double.parseDouble(jTextField1.getText());
    }
    catch(NumberFormatException e){
        e.printStackTrace();
        
    }
    
    price = convert(tickerCurrency1, tickerCurrency2, currencies, amount);
    
    formattedPrice = format.format(price);
    formattedAmount = format.format(amount);
    
    result = formattedAmount + " " + tickerCurrency1 + " = " + formattedPrice + " " + tickerCurrency2;
    jTextField2.setText(result);

    }//GEN-LAST:event_ConvertButtonActionPerformed

