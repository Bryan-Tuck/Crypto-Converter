    public static void populate(JComboBox<String> comboBox, ArrayList<Transactions> currencies){
        for(int i = 0; i < currencies.size(); i++){
            comboBox.addItem(currencies.get(i).getName());
        }
    }
        
    public static Double convert(String currency1, String currency2, ArrayList<Transactions> currencies, Double amount){
            String tickerNameCurrency2 = null;
            double exchangeValue;
            Double price = 0.0;
            
            for(int i = 0; i < currencies.size(); i++){
                if(currencies.get(i).getName() == currency2){
                    tickerNameCurrency2 = currencies.get(i).getTicker();
                    break;
                }
            }
            
            if(tickerNameCurrency2 != null){
                for(int i = 0; i < currencies.size(); i++){
                    if(currencies.get(i).getName() == currency1){
                        exchangeValue = currencies.get(i).getExchangeValues().get(tickerNameCurrency2);
                        price = Transactions.convert(amount, exchangeValue);
                        break;
                    }
                }
            }
            return price;
        }
    
    

